#include <stdio.h>

int main()
{
    int a, b;
    // Relational operators
    a >= b; // true if a is equal to b or a is greater than b
    a > b;  // true if a is greater than b
    a == b; // true if a equals b
    a < b;  // true if a is less than b
    a <= b; // true if a is less than b or a is equal to b
    a != b; // true if a is different from b
    // Logical operators
    a &&b;  //  a and b: true if and only if a is true and b is true
    a || b; // a or b
    !a;     // Not a
}